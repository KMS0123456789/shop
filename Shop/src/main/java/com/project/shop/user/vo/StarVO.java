package com.project.shop.user.vo;

public class StarVO {
	private int starNo;
	private String starUser;
	private int computerNo;
	private int peripheralNo;
	private int starCount;
	private int itemCategory;
	
	public int getStarNo() {
		return starNo;
	}
	public String getStarUser() {
		return starUser;
	}
	public int getComputerNo() {
		return computerNo;
	}
	public int getPeripheralNo() {
		return peripheralNo;
	}
	public int getStarCount() {
		return starCount;
	}
	public int getItemCategory() {
		return itemCategory;
	}
	public void setStarNo(int starNo) {
		this.starNo = starNo;
	}
	public void setStarUser(String starUser) {
		this.starUser = starUser;
	}
	public void setComputerNo(int computerNo) {
		this.computerNo = computerNo;
	}
	public void setPeripheralNo(int peripheralNo) {
		this.peripheralNo = peripheralNo;
	}
	public void setStarCount(int starCount) {
		this.starCount = starCount;
	}
	public void setItemCategory(int itemCategory) {
		this.itemCategory = itemCategory;
	}
}
