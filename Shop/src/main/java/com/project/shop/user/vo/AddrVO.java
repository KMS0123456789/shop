package com.project.shop.user.vo;

public class AddrVO {
	private int addrNo;
	private String addrUser;
	private String address;
	private int addrFlag;
	
	public int getAddrNo() {
		return addrNo;
	}
	public String getAddrUser() {
		return addrUser;
	}
	public String getAddress() {
		return address;
	}
	public int getAddrFlag() {
		return addrFlag;
	}
	public void setAddrNo(int addrNo) {
		this.addrNo = addrNo;
	}
	public void setAddrUser(String addrUser) {
		this.addrUser = addrUser;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public void setAddrFlag(int addrFlag) {
		this.addrFlag = addrFlag;
	}
	
	
}
